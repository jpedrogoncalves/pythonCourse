# arithmetic operators
adicao = 10 + 10
print(adicao)

subtracao = 10 - 2
print(subtracao)

multiplicacao = 10 * 10
print(multiplicacao)

divisao = 6 / 3.3 # sempre retorna float.
print(divisao)

divisao_inteira = 10 // 4
print(divisao_inteira)

exponenciacao = 2 ** 10
print(exponenciacao)

modulo = 55 % 2
print(modulo)
